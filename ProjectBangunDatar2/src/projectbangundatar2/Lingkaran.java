public class Lingkaran {
    
    double luasLingkaran(int r) {
        return((double)22/7 * r * r);
    }
    
    double luasLingkaran(double r) {
        return((double)22/7 * r * r);
    }
    
    double kelilingLingkaran(int r) {
        return(2 * (double)22/7 * r);
    }
    
    double kelilingLingkaran(double r) {
        return(2 * (double)22/7 * r);
    }
}
